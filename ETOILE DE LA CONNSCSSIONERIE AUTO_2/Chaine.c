int strcont(char a[],char c) // verifie si le caractere c est dans la chaine a (1=Vrai 0=Faux)
{
    int i,v=0;
    for (i=0;i<strlen(a);i++)
     if (toupper(a[i])==toupper(c)) v=1;
     return v;
}
/********************************************************************************************************************************/

void strsub (char a[],char b[],char c) // mettre tous les caracteres c qui sont dans a dans b (memes positions)
{
    int i;
    for (i=0;i<strlen(a);i++)
        if (toupper(a[i])==toupper(c)) b[i]=a[i];
}
