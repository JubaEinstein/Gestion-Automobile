int scans(char s[],int a) // pour lire une chaine
{
    char *c;
    if (fgets(s,a,stdin)!=NULL)
    {
        c=strchr(s,'\n');
        if (c!=NULL) *c='\0';
        else clearbuff();
        return 1;
    }
/********************************************************************************************************************************/

    else
    {
        clearbuff();
        return 0;
    }

}
/********************************************************************************************************************************/

void clearbuff() // Vider le buffer
{
    int c = 0;
    while (c != '\n' && c != EOF)
    {
        c = getchar();
    }
}
/********************************************************************************************************************************/

long scanl()
{
    char s[100]="";
    if (scans(s,100)) return strtol(s,NULL,10);
    else return 0;
}
