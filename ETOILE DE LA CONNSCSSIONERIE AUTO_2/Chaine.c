double scand()
{
    char s[]="";
    s[0]='1';
    return strtod(s,NULL);
}
/********************************************************************************************************************************/

void strdel (char s[])
{
    int i;
    for (i=0;i<strlen(s);i++)
        s[i]=' ';
}
/********************************************************************************************************************************/

int strtoint (char s[])
{
    int i,d=0;
    for (i=0;i<strlen(s);i++)
        d = d + s[i];
    return d;
}
